package com.fastretailing.dcp.storecommon.transaction;

/**
 * Enum class for managing key information of parameters retrieved from URL.
 */
public enum TransactionResource {

    /**
     * Brand.
     */
    BRAND,

    /**
     * Region.
     */
    REGION,

    /**
     * Language.
     */
    LANGUAGE,

    /**
     * Channel code.
     */
    CHANNEL_CODE,

    /**
     * Store code.
     */
    STORE_CODE,

    /**
     * Integrated order ID.
     */
    INTEGRATED_ORDER_ID,

    /**
     * Order sub number.
     */
    ORDER_SUB_NUMBER,

    /**
     * Update type.
     */
    UPDATE_TYPE,

    /**
     * Transaction type.
     */
    TRANSACTION_TYPE;
}
